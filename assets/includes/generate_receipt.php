                        </td>
                    </tr>
                </table>
            </div>

            <div class="body">
                <table class="meta-wrap">
                    <tr>
                        <td><div class="meta-card"><div class="meta-label">Receipt No</div><div class="meta-value"><?= h($receiptNo) ?></div></div></td>
                        <td><div class="meta-card"><div class="meta-label">Booking Ref</div><div class="meta-value"><?= h($referenceNumber) ?></div></div></td>
                        <td><div class="meta-card"><div class="meta-label">Generated At</div><div class="meta-value"><?= h($generatedAtFormatted) ?></div></div></td>
                        <td><div class="meta-card"><div class="meta-label">Generated By</div><div class="meta-value"><?= h($generatedByName) ?></div></div></td>
                    </tr>
                </table>

                <table class="amount-panel">
                    <tr>
                        <td>
                            <div class="amount-left-title">Payment successfully collected</div>
                            <div class="amount-left-sub">This receipt confirms the collected amount for the related Airport Parking booking.</div>
                            <div class="paid-badge"><?= h($paymentStatus) ?></div>
                        </td>
                        <td class="amount-right">
                            <div class="amount-label">Payment Amount</div>
                            <div class="amount-value">LKR <?= number_format($balanceAmount, 2) ?></div>
                        </td>
                    </tr>
                </table>

                <table class="section-table">
                    <tr>
                        <td>
                            <div class="section-box">
                                <div class="section-title">Customer Details</div>
                                <table class="details">
                                    <tr><td>Customer Name</td><td><?= h($customerName) ?></td></tr>
                                    <tr><td>Contact Number</td><td><?= h($contactNumber) ?></td></tr>
                                    <tr><td>Vehicle Number</td><td><?= h($vehicleNumber) ?></td></tr>
                                    
                                </table>
                            </div>
                        </td>
                        <td>
                            <div class="section-box">
                                <div class="section-title">Receipt Details</div>
                                <table class="details">
                                    <tr><td>Receipt Number</td><td><?= h($receiptNo) ?></td></tr>
                                    <tr><td>Booking Reference</td><td><?= h($referenceNumber) ?></td></tr>
                                    <tr><td>Parking Duration</td><td><?= h($expectedParkingDuration) ?></td></tr>
                                    <tr><td>System Date & Time</td><td><?= h($generatedAtFormatted) ?></td></tr>
                                </table>
                            </div>
                        </td>
                    </tr>
                </table>

                <div class="footer">
                    <table class="footer-table">
                        <tr>
                            <td class="footer-note">
                                This is a system-generated receipt issued by <?= h($companyName) ?>.
                                Please retain this document for your records and future reference.
                            </td>
                            <td class="signature-box">
                                <div class="signature-line"></div>
                                <div class="signature-name"><?= h($companyName) ?></div>
                                <div class="signature-role">Authorized Signature</div>
                            </td>
                        </tr>
                    </table>
                </div>
            </div>
        </div>
    </body>
