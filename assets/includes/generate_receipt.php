    <?php
    $pdfHtml = ob_get_clean();

    $options = new Options();
    $options->set('isRemoteEnabled', true);

    $dompdf = new Dompdf($options);
    $dompdf->loadHtml($pdfHtml);
    $dompdf->setPaper('A4', 'portrait');
    $dompdf->render();

    // save PDF to folder: /payment-receipts/{reference_number}/
    $safeReference = safeFolderName($referenceNumber);
    $relativeFolder = 'payment-receipts/' . $safeReference;
    $absoluteFolder = dirname(__DIR__, 2) . '/' . $relativeFolder;

    if (!is_dir($absoluteFolder)) {
        mkdir($absoluteFolder, 0777, true);
    }

    $fileName = 'receipt_' . $receiptNo . '.pdf';
    $absoluteFilePath = $absoluteFolder . '/' . $fileName;
    $relativeFilePath = $relativeFolder . '/' . $fileName;

    file_put_contents($absoluteFilePath, $dompdf->output());

    $pathStmt = $conn->prepare("
        UPDATE payment_receipts
        SET receipt_path = ?
        WHERE id = ?
    ");
    $pathStmt->execute([$relativeFilePath, $receiptId]);

    $conn->commit();

    echo json_encode([
        'success' => true,
        'receipt_id' => $receiptId,
        'balance_amount' => $balanceAmount,
        'receipt_path' => $relativeFilePath
    ]);
} catch (Throwable $e) {
    if ($conn->inTransaction()) {
        $conn->rollBack();
    }

    echo json_encode([
        'success' => false,
        'message' => 'Error generating receipt: ' . $e->getMessage()
    ]);
}